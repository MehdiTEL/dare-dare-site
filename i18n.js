/* ===== CURRENT LANGUAGE ===== */
let currentLang = localStorage.getItem('dare-dare-lang') || 'fr';

/* ===== SWITCH LANGUAGE ===== */
function switchLanguage(lang) {
  if (lang !== 'fr' && lang !== 'en') return;
  currentLang = lang;
  localStorage.setItem('dare-dare-lang', lang);
  document.documentElement.lang = lang === 'fr' ? 'fr' : 'en';

  /* 1. Translate all data-i18n elements */
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.dataset.i18n;
    const t = translations[key];
    if (!t) return;
    const val = t[lang];
    if (val === undefined) return;

    /* Check which attribute to set */
    if (el.dataset.i18nAttr) {
      el.setAttribute(el.dataset.i18nAttr, val);
    } else if (el.hasAttribute('data-i18n-placeholder')) {
      el.placeholder = val;
    } else {
      el.innerHTML = val;
    }
  });

  /* 2. Update event data (hero, services, bio) */
  updateEventDataForLang(lang);
  if (typeof window.currentType !== 'undefined' && typeof window.switchEventType === 'function') {
    window.switchEventType(window.currentType);
  } else {
    /* Fallback: directly update hero if script.js hasn't exposed switchEventType yet */
    const type = window.currentType || 'soiree';
    const i18n = eventDataI18n[type];
    if (i18n) {
      const heroTitle = document.getElementById('hero-title');
      const heroSubtitle = document.getElementById('hero-subtitle');
      const bioDynamic = document.getElementById('bio-dynamic');
      if (heroTitle) heroTitle.textContent = i18n.heroTitle[lang];
      if (heroSubtitle) heroSubtitle.textContent = i18n.heroSubtitle[lang];
      if (bioDynamic) bioDynamic.textContent = i18n.bio[lang];
    }
  }

  /* 3. Update testimonials */
  updateTestimonialsForLang(lang);

  /* 4. Update FAQ */
  updateFaqForLang(lang);

  /* 5. Update guest mood */
  updateGuestMoodForLang(lang);

  /* 6. Update toggle buttons visual state */
  document.querySelectorAll('.lang-toggle__btn').forEach(btn => {
    btn.classList.toggle('active', btn.dataset.lang === lang);
  });
}

/* Update event data object in script.js */
function updateEventDataForLang(lang) {
  Object.keys(eventDataI18n).forEach(type => {
    const i18n = eventDataI18n[type];
    const data = window.eventData?.[type];
    if (!data) return;
    data.heroTitle = i18n.heroTitle[lang];
    data.heroSubtitle = i18n.heroSubtitle[lang];
    data.bio = i18n.bio[lang];
    if (i18n.services) {
      i18n.services.forEach((s, idx) => {
        if (data.services[idx]) {
          data.services[idx].title = s.title[lang];
          data.services[idx].desc = s.desc[lang];
          data.services[idx].features = s.features[lang];
        }
      });
    }
  });
}

/* Update testimonials in DOM */
function updateTestimonialsForLang(lang) {
  const slides = document.querySelectorAll('.temoignage-slide');
  slides.forEach((slide, i) => {
    if (!testimonialsI18n[i]) return;
    const textEl = slide.querySelector('.temoignage-slide__text');
    const authorEl = slide.querySelector('.temoignage-slide__author');
    if (textEl) textEl.textContent = testimonialsI18n[i].text[lang];
    if (authorEl) authorEl.textContent = testimonialsI18n[i].author[lang];
  });
  /* Update the JS testimonials array too */
  if (window.testimonials) {
    testimonialsI18n.forEach((t, i) => {
      if (window.testimonials[i]) {
        window.testimonials[i].text = t.text[lang];
        window.testimonials[i].author = t.author[lang];
      }
    });
  }
}

/* Update FAQ in DOM */
function updateFaqForLang(lang) {
  const faqItems = document.querySelectorAll('.faq-item');
  faqItems.forEach((item, i) => {
    if (!faqI18n[i]) return;
    const qSpan = item.querySelector('.faq-item__question span:first-child');
    const aInner = item.querySelector('.faq-item__answer-inner');
    if (qSpan) qSpan.textContent = faqI18n[i].q[lang];
    if (aInner) aInner.textContent = faqI18n[i].a[lang];
  });
}

/* Update guest mood text for current value */
function updateGuestMoodForLang(lang) {
  const guestRange = document.getElementById('guest-count');
  const guestMood = document.getElementById('guest-mood');
  if (!guestRange || !guestMood) return;
  const v = parseInt(guestRange.value);
  for (const mood of guestMoodI18n) {
    if (v <= mood.max) {
      guestMood.textContent = mood[lang];
      break;
    }
  }
}

/* ===== INIT ON LOAD ===== */
/* Use a small delay to ensure script.js has initialized first */
document.addEventListener('DOMContentLoaded', () => {
  /* Bind toggle buttons immediately */
  document.querySelectorAll('.lang-toggle__btn').forEach(btn => {
    btn.addEventListener('click', () => switchLanguage(btn.dataset.lang));
  });

  /* Apply saved language AFTER script.js has fully initialized
     script.js uses setTimeout(300) for hero fade animation, so we wait 500ms */
  setTimeout(() => {
    if (currentLang !== 'fr') {
      switchLanguage(currentLang);
    }
    /* Update toggle button visual state */
    document.querySelectorAll('.lang-toggle__btn').forEach(btn => {
      btn.classList.toggle('active', btn.dataset.lang === currentLang);
    });
  }, 500);
});
