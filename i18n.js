/* ----- Update portfolio for mode ----- */
function updatePortfolioForMode(mode) {
  const grid = document.querySelector('.portfolio__grid');
  const filters = document.querySelector('.portfolio__filters');
  const section = document.querySelector('.portfolio') || document.getElementById('portfolio');
  if (!section) return;

  let comingSoon = section.querySelector('.portfolio__coming-soon');

  if (mode === 'prive') {
    if (grid) grid.style.display = 'none';
    if (filters) filters.style.display = 'none';
    if (!comingSoon) {
      comingSoon = document.createElement('div');
      comingSoon.className = 'portfolio__coming-soon';
      const insertTarget = grid || filters;
      if (insertTarget) insertTarget.parentNode.insertBefore(comingSoon, insertTarget);
    }
    comingSoon.style.display = '';
    const text = translations['portfolio.prive.soon']?.[currentLang] || '';
    comingSoon.innerHTML = `<p>${text}</p>`;
  } else {
    if (grid) grid.style.display = '';
    if (filters) filters.style.display = '';
    if (comingSoon) comingSoon.style.display = 'none';
  }
}

/* ----- Update i18n keys for mode ----- */
function updateI18nForMode(mode) {
  document.querySelectorAll('[data-i18n-prive]').forEach(el => {
    if (mode === 'prive') {
      /* Store original key if not yet stored */
      if (!el.dataset.i18nPro) {
        el.dataset.i18nPro = el.dataset.i18n;
      }
      el.dataset.i18n = el.dataset.i18nPrive;
    } else {
      /* Restore original key */
      if (el.dataset.i18nPro) {
        el.dataset.i18n = el.dataset.i18nPro;
      }
    }
  });
}

/* ===== INIT ON LOAD ===== */
/* Use a small delay to ensure script.js has initialized first */
document.addEventListener('DOMContentLoaded', () => {
  /* Save Pro (B2B) references before any mode switch */
  window.eventDataPro = window.eventData;
  window.testimonialsPro = window.testimonials;

  /* Bind language toggle buttons */
  document.querySelectorAll('.lang-toggle__btn').forEach(btn => {
    btn.addEventListener('click', () => switchLanguage(btn.dataset.lang));
  });

  /* Bind mode toggle buttons */
  document.querySelectorAll('.mode-toggle__btn').forEach(btn => {
    btn.addEventListener('click', () => switchMode(btn.dataset.mode));
  });

  /* Apply saved language & mode AFTER script.js has fully initialized
     script.js uses setTimeout(300) for hero fade animation, so we wait 500ms */
  setTimeout(() => {
    /* Apply saved mode if not default */
    if (currentMode === 'prive') {
      switchMode('prive');
    }

    if (currentLang !== 'fr') {
      switchLanguage(currentLang);
    }

    /* Update toggle button visual states */
    document.querySelectorAll('.lang-toggle__btn').forEach(btn => {
      btn.classList.toggle('active', btn.dataset.lang === currentLang);
    });
    document.querySelectorAll('.mode-toggle__btn').forEach(btn => {
      btn.classList.toggle('active', btn.dataset.mode === currentMode);
    });
  }, 500);
});
