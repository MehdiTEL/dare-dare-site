/* ===== SWITCH MODE (B2B ↔ B2C) ===== */
function switchMode(mode) {
  if (mode !== 'pro' && mode !== 'prive') return;
  currentMode = mode;
  localStorage.setItem('dare-dare-mode', mode);

  /* 1. Update mode-toggle button states + body class for color inversion */
  document.querySelectorAll('.mode-toggle__btn').forEach(btn => {
    btn.classList.toggle('active', btn.dataset.mode === mode);
  });
  document.body.classList.toggle('mode-prive', mode === 'prive');

  /* 2. Swap eventData */
  if (mode === 'prive') {
    window.eventData = window.eventDataPrive;
    window.testimonials = window.testimonialsPrive;
  } else {
    window.eventData = window.eventDataPro;
    window.testimonials = window.testimonialsPro;
  }

  /* 3. Rebuild hero tabs */
  rebuildHeroTabs(mode);

  /* 4. Switch to first event type */
  const firstType = Object.keys(window.eventData)[0];
  if (typeof window.switchEventType === 'function') {
    window.switchEventType(firstType);
  }

  /* 5. Rebuild testimonials */
  rebuildTestimonials(mode);

  /* 6. Rebuild FAQ */
  rebuildFaq(mode);

  /* 7. Rebuild contact Step 1 cards */
  rebuildContactCards(mode);

  /* 8. Rebuild contact Step 2 tags */
  rebuildContactTags(mode);

  /* 9. Swap data-i18n keys for mode-aware elements */
  updateI18nForMode(mode);

  /* 10. Toggle portfolio visibility */
  updatePortfolioForMode(mode);

  /* 11. Update hidden mode field for form submission */
  const modeField = document.getElementById('mode-field');
  if (modeField) modeField.value = mode;

  /* 12. Re-apply current language */
  switchLanguage(currentLang);
}

/* ----- Rebuild hero tabs ----- */
function rebuildHeroTabs(mode) {
  const tabsContainer = document.querySelector('.hero__tabs');
  if (!tabsContainer) return;
  const types = Object.keys(window.eventData);
  let html = '';
  types.forEach((type, i) => {
    const i18nKey = mode === 'prive' ? `hero.tab.${type}` : `hero.tab.${type}`;
    const label = translations[i18nKey]?.[currentLang] || type;
    const isFirst = i === 0;
    if (i > 0) html += '<span class="hero__tab-diamond" aria-hidden="true">\u25C6</span>';
    html += `<button class="hero__tab${isFirst ? ' active' : ''}" role="tab" aria-selected="${isFirst}" data-type="${type}" id="tab-${type}" data-i18n="${i18nKey}">${label}</button>`;
  });
  tabsContainer.innerHTML = html;

  /* Re-bind click listeners */
  tabsContainer.querySelectorAll('.hero__tab').forEach(tab => {
    tab.addEventListener('click', () => {
      if (typeof window.switchEventType === 'function') {
        window.switchEventType(tab.dataset.type);
      }
    });
  });
}

